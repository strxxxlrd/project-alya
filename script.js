// Current language (default: Ukrainian)
let currentLang = localStorage.getItem('lang') || 'uk';

// Apply translations to the page
function applyTranslations() {
    const elements = document.querySelectorAll('[data-i18n]');
    const isMobile = window.innerWidth <= 768;
    
    elements.forEach(el => {
        let key = el.getAttribute('data-i18n');
        
        // Проверяем мобильную версию
        if (isMobile && el.classList.contains('mobile')) {
            const mobileKey = key + '-mobile';
            if (translations[currentLang] && translations[currentLang][mobileKey]) {
                key = mobileKey;
            }
        }
        // Проверяем версию для рабочего стола
        else if (!isMobile && el.classList.contains('desktop')) {
            const desktopKey = key + '-desktop';
            if (translations[currentLang] && translations[currentLang][desktopKey]) {
                key = desktopKey;
            }
        }
        
        if (translations[currentLang] && translations[currentLang][key]) {
            // Используем innerHTML для сохранения тегов <br />
            el.innerHTML = translations[currentLang][key];
        }
    });
    
    // Обновляем текст кнопки языка
    const langBtn = document.querySelector('.language span');
    if (langBtn) {
        langBtn.textContent = currentLang === 'uk' ? 'UA' : 'EN';
    }
    
    // Обновляем класс кнопки для стилизации
    const langBtnContainer = document.querySelector('.language');
    if (langBtnContainer) {
        langBtnContainer.setAttribute('data-lang', currentLang);
    }
    
    // Сохраняем выбор языка
    localStorage.setItem('lang', currentLang);
}

// Переключение языка
function toggleLanguage() {
    currentLang = currentLang === 'uk' ? 'en' : 'uk';
    applyTranslations();
}

// Инициализация языка при загрузке страницы
document.addEventListener('DOMContentLoaded', () => {
    applyTranslations();
    
    // Добавляем обработчик клика на кнопку языка
    const langBtn = document.querySelector('.language');
    if (langBtn) {
        langBtn.addEventListener('click', toggleLanguage);
    }
});

