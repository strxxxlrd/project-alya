// COOKIE BANNER
// ================================================
document.addEventListener('DOMContentLoaded', () => {
    const banner = document.getElementById('cookie-banner');
    const acceptBtn = document.getElementById('accept-btn');
    const rejectBtn = document.getElementById('reject-btn');

    if (!banner) return;

    function hasConsent() {
        return localStorage.getItem('cookie_consent') !== null;
    }

    // Показываем только если еще не было выбора
    // и только после того как загрузчик закрылся
    if (!hasConsent()) {
        // Ждем пока загрузчик закроется
        const waitForLoader = setInterval(() => {
            const loader = document.getElementById('loader');
            if (!loader || loader.style.display === 'none') {
                clearInterval(waitForLoader);
                setTimeout(() => {
                    banner.classList.add('show');
                }, 500);
            }
        }, 100);
    }

    if (acceptBtn) {
        acceptBtn.addEventListener('click', () => {
            localStorage.setItem('cookie_consent', 'accepted');
            banner.classList.remove('show');
        });
    }

    if (rejectBtn) {
        rejectBtn.addEventListener('click', () => {
            localStorage.setItem('cookie_consent', 'rejected');
            banner.classList.remove('show');
        });
    }
});

