const video = document.getElementById('myVideo');
const videoSection = document.getElementById('videoSection');

// Включаем видео только когда секция попадает в viewport
const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            video.play().catch(e => console.log("Автовоспроизведение заблокировано браузером"));
        } else {
            video.pause();
        }
    });
}, {
    threshold: 0.6   // видео запускается, когда 60% секции видно
});

observer.observe(videoSection);

// Запрещаем пользователю ставить на паузу
video.addEventListener('pause', () => {
    // Если пользователь пытается поставить на паузу — сразу запускаем обратно
    setTimeout(() => {
        if (video.paused) {
            video.play();
        }
    }, 100);
});

// Дополнительная защита (на случай, если пользователь нажмёт пробел или кликнет)
video.addEventListener('click', (e) => {
    e.preventDefault();
    if (video.paused) video.play();
});






